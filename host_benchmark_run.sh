#!/bin/bash
#This runs benchmark on the host
#host_benchmark_run.sh [ARGS]

ARGS="$@"
TESTCASES=`ls -d host/*/*run.sh`
for testcase in ${TESTCASES}
do
    TEST_NAME=$( dirname ${testcase} | sed 's/host\///g')
    echo "********************************************************************"
    echo "********************************************************************"
    echo "START Testcase: ${TEST_NAME}"
    echo "********************************************************************"
    echo "********************************************************************"
    ./${testcase} ${ARGS}
    echo "********************************************************************"
    echo "********************************************************************"
    echo "END Testcase: ${TEST_NAME}"
    echo "********************************************************************"
    echo "********************************************************************"
done

